import { Schema, model } from 'mongoose';

const FoodSchema = new Schema({
  name: {
    type: String,
    required: true,
  },
  description: {
    type: String,
    required: true,
  },
  price: {
    type: Number,
    required: true,
  },
  image: {
    type: String,
    required: true,
  },
  category: {
    type: String,
    required: true,
  },
});

const FoodModel = model.food || model('food', FoodSchema);
export default FoodModel;
